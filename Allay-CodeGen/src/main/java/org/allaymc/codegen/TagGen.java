package org.allaymc.codegen;

import com.squareup.javapoet.*;

import javax.lang.model.element.Modifier;
import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.Arrays;
import java.util.HashMap;
import java.util.stream.Collectors;

import static org.allaymc.codegen.ClassNames.*;

/**
 * Allay Project 2024/08/27
 *
 * @author IWareQ
 */
public class TagGen {
    public static void main(String[] args) throws IOException {
        generate("Allay-API/src/main/java/org/allaymc/api/item/tag/ItemTags.java", ITEM_TAG, "unpacked/item_tags.json", "item_tags_custom.json");
        generate("Allay-API/src/main/java/org/allaymc/api/block/tag/BlockTags.java", BLOCK_TAG, "unpacked/block_tags.json", "block_tags_custom.json");
    }

    public static void generate(String writeTo, ClassName tagClass, String... tagFiles) throws IOException {
        var interfaceBuilder = TypeSpec.interfaceBuilder(tagClass.simpleName() + "s")
                .addJavadoc("Automatically generated by {@code org.allaymc.codegen.TagGen}")
                .addModifiers(Modifier.PUBLIC)
                .addField(
                        FieldSpec.builder(ParameterizedTypeName.get(MAP, STRING, tagClass), "NAME_TO_TAG")
                                .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                                .initializer("new $T<>()", HashMap.class)
                                .build()
                );

        var keys = Arrays.stream(tagFiles)
                .flatMap(file -> Utils.parseKeys(Path.of(CodeGen.DATA_PATH + file)).stream())
                .collect(Collectors.toSet());
        for (var key : keys) {
            var fieldName = key.split(":")[1].toUpperCase();
            interfaceBuilder.addField(
                    FieldSpec.builder(tagClass, fieldName)
                            .addModifiers(Modifier.PUBLIC, Modifier.STATIC, Modifier.FINAL)
                            .initializer("create($S)", key)
                            .build()
            );
        }

        interfaceBuilder.addMethod(
                MethodSpec.methodBuilder("create")
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .returns(tagClass)
                        .addParameter(String.class, "name")
                        .addStatement("var tag = new $T(name)", tagClass)
                        .addStatement("NAME_TO_TAG.put(name, tag)")
                        .addStatement("return tag")
                        .build()
        ).addMethod(
                MethodSpec.methodBuilder("getTagByName")
                        .addModifiers(Modifier.PUBLIC, Modifier.STATIC)
                        .returns(tagClass)
                        .addParameter(String.class, "name")
                        .addStatement("return NAME_TO_TAG.get(name)")
                        .build()
        );

        var javaFile = JavaFile.builder(tagClass.packageName(), interfaceBuilder.build())
                .indent(Utils.INDENT)
                .skipJavaLangImports(true)
                .build();
        Files.writeString(Path.of(writeTo), javaFile.toString());
    }
}
