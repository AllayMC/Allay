package org.allaymc.api.entity.property.enums;

/**
 * Represents the climate variant of an entity, determined by the biome where it spawns.
 * Used by pig, cow, chicken, and egg entities in Bedrock Edition 1.21.80+.
 *
 * @author daoge_cmd
 */
public enum ClimateVariant {
    TEMPERATE,
    WARM,
    COLD
}
